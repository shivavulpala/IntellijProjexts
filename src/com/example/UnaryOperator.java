package com.example;

public class UnaryOperator {
    public static void main(String args[])
    {
        int a=+4;
        System.out.println(a);

        a--;
        System.out.println(a);

        a++;
        System.out.println(a);

        a=-a;
        System.out.println(a);

        boolean b=false;
        System.out.println(b);
        System.out.println(!b);

        int x=10;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);



    }
}
