package com.example;

public class ComparisionOperator {
    public static void main(String args[])
    {
        int a=1;
        int b=2;

        if(a==b)
            System.out.println("a==b");
        if(a!=b)
            System.out.println("a!=b");
        if(a>b)
            System.out.println("a>b");
        if(a<b)
            System.out.println("a<b");
        if(a>=b)
            System.out.println("a>=b");
        if(a<=b)
            System.out.println("a<=b");

    }
}
