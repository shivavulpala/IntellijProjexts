package com.example;

public class OneDArray {
    public static void main(String args[])
    {
        int Array[]= new int[4];//initialize & allocation
        Array [0]=10;//assigning/initializing value
        Array [1]=20;
        Array [2]=30;
        Array [3]=40;

        System.out.println("Value at index Array[0] is:" +Array[0]);
        System.out.println(Array[2]);
        System.out.println("Value at index 0 element is:" +Array[3]);
    }
}
