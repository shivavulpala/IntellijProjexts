package com.example;

public class ArrayDemo {
    public static void main(String args[])
    {
        int Array[];//declaration//Array - variable name
        Array = new int[10];//allocation
        Array [0]=10;//assigning/initializing value
        Array [1]=20;
        Array [2]=30;
        Array [3]=40;
        Array [4]=50;
        Array [5]=60;
        Array [6]=70;
        Array [7]=80;
        Array [8]=90;
        Array [9]=100;
        System.out.println("Value at index Array[0] is:" +Array[0]);
        System.out.println(Array[2]);
        System.out.println("Value at index 0 element is:" +Array[9]);
    }

}
